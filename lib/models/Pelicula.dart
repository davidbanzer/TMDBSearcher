class Pelicula {
  String? titulo;
  String? lanzamiento;
  String? sinopsis;
  String? imagen;
  num? calificacion;

  Pelicula(titulo, lanzamiento, sinopsis, imagen, calificacion) {
    this.titulo = titulo;
    this.lanzamiento = lanzamiento;
    this.sinopsis = sinopsis;
    this.imagen = imagen;
    this.calificacion = calificacion;
  }
}
